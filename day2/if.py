#short hand if:  If you have only one statement to execute, you can put it on the same line as the if statement.
a = 10
b = 10
if a > b: print("a is greater than b")
else: print('a is not greater than b')             #else is optional obviously.



#Short Hand If ... Else
print('Good morning A') if a>=b else print('Good evening B') 


